const add = (a, b) => {
  return a + b;
};

const divide = (a, b) => {
  return a / b;
};
const multiply = (a, b) => {
  return a * b;
};

const mod = (a, b) => {
  return a % b;
};

const exponent = (a, b) => {
  return a ** b;
};

const subt = (a, b) => {
  return a - b;
};

console.log(add(1, 2), 3);
console.log(multiply(1, 2), 2);
console.log(divide(2, 1), 2);
console.log(mod(1, 2), 1);
console.log(exponent(1, 2), 1);
console.log(subt(1, 2), -1);
